    ### HELPERS ###
    @staticmethod
    def map_dict(dict, obj):
        o = obj()
        if dict is None:
            return o

        for p in dict:
            if hasattr(o, p):
                setattr(o, p, dict[p])
        return o

    @staticmethod
    def map_list_of_dict(lst, obj):
        ret = list()
        if len(lst) == 0:
            return ret

        for l in lst:
            ret.append(Dataflows.map_dict(l, obj))
        return ret
