class Dataflows:

    @staticmethod
    def get_xml(type: str, year: int, timezone: str, description: str):
        if type.upper() == "B":
            return Dataflows.get_dataflowB(year, timezone, description)
        if type.upper() == "C":
            return Dataflows.get_dataflowC(year, timezone, description)
        if type.upper() == "D":
            return Dataflows.get_dataflowD(year, timezone, description)
        if type.upper() == "E1A":
            return Dataflows.get_dataflowE1A(year, timezone, description)
        if type.upper() == "G":
            return Dataflows.get_dataflowG(year, timezone, description)
        return None

    ### DATAFLOWS ###

    @staticmethod
    def get_dataflowB(year: int, timezone: int, description: str):
        schema = Dataflows.get_schema()
        reporter = Dataflows.get_responsible_reporter(year, timezone, description)
        zones = Dataflows.get_zones()
        pollutants = Dataflows.get_pollutants()
        return schema.get_dataflow_b(reporter, zones, pollutants, timezone)

    @staticmethod
    def get_dataflowC(year: int, timezone: int, description: str):
        schema = Dataflows.get_schema()
        reporter = Dataflows.get_responsible_reporter(year, timezone, description)
        assessmentregimes = Dataflows.get_assessmentregimes()
        assessmentmethods = Dataflows.get_assessmentmethods()
        return schema.get_dataflow_c(reporter, assessmentregimes, assessmentmethods, timezone)

    @staticmethod
    def get_dataflowD(year: int, timezone: int, description: str):
        schema = Dataflows.get_schema()
        reporter = Dataflows.get_responsible_reporter(year, timezone, description)
        networks = Dataflows.get_networks()
        stations = Dataflows.get_stations()
        samples = Dataflows.get_samples()
        processes = Dataflows.get_processes()

        sampling_points = Dataflows.get_samplingpoints()
        for s in sampling_points:  # This is slow
            s.observing_capabilities = Dataflows.get_observing_capabilities(s.id)

        return schema.get_dataflow_d(reporter, networks, stations, sampling_points, samples, processes)

    @staticmethod
    def get_dataflowE1A(year: int, timezone: int, description: str):
        schema = Dataflows.get_schema()
        reporter = Dataflows.get_responsible_reporter(year, timezone, description)
        observations = Dataflows.get_observations_e1a(year)
        return schema.get_dataflow_e(reporter, observations)

    @staticmethod
    def get_dataflowE2A(last_request: str):
        schema = Dataflows.get_schema()
        observations = Dataflows.get_observations_e2a(last_request)
        if len(observations) == 0:
            raise Exception("No data available to create xml")
        reporter = Dataflows.get_responsible_reporter_simple()
        reporter.change = True
        reporter.begin = min([x.min_end_position for x in observations])
        reporter.end = max([x.max_end_position for x in observations])

        return schema.get_dataflow_e(reporter, observations)

    @staticmethod
    def get_dataflowG(year: int, timezone: int, description: str):
        schema = Dataflows.get_schema()
        reporter = Dataflows.get_responsible_reporter(year, timezone, description)
        attainments = Dataflows.get_attainments()
        exceedancedescriptions = Dataflows.get_exceedancedescriptions()
        exceedingmethods = Dataflows.get_exceedingmethods()
        return schema.get_dataflow_g(reporter, attainments, exceedancedescriptions, exceedingmethods, timezone)

    ### LOOKUPS ##
    @staticmethod
    def get_schema():
        with CursorFromPool() as cursor:
            cursor.execute("select * from settings")
            row = cursor.fetchone()
            return Dataflows.map_dict(row, Schema)

    @staticmethod
    def get_responsible_reporter(year: int, timezone: int, description: str):
        reporter = Dataflows.get_responsible_reporter_simple()
        reporter.change = True
        reporter.description = description if description else "XML generated by Raven"
        reporter.begin = str(year) + "-01-01T00:00:00+0" + str(timezone) + ":00"
        reporter.end = str(year+1) + "-01-01T00:00:00+0" + str(timezone) + ":00"
        return reporter

    @staticmethod
    def get_responsible_reporter_simple():
        with CursorFromPool() as cursor:
            cursor.execute("""
                select *
                from responsible_authorities
                where is_responsible_reporter = true
              """)
            row = cursor.fetchone()
            reporter = Dataflows.map_dict(row, ResponsibleReporter)
            return reporter

